#include <bits/stdc++.h>
using namespace std;
int main(){
    cin.tie(nullptr)->sync_with_stdio(false);

    int t; cin>>t;

    for(t; t>0;t--){

        int n; cin>>n;

        for(int i =1;i<=n;i++){
            cout<<n<<" ";
        }
        cout<<"\n";


    }

    return 0;
}